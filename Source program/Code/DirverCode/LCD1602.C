/* *******************************************************************************************
// 设置LCD1602坐标
/* ******************************************************************************************* */
void LCD1602_GoTo_xy(u8 y, u8 x)
{
    switch (y)
    {
        case 1: writeCommand(0x80 + x);         //第一行
            break;
        case 2: writeCommand(0x80 + 0x40 + x);  //第二行
            break;
        default:
            break;
    }
}