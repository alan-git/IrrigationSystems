/* *******************************************************************************************
// US级延时
/* ******************************************************************************************* */
void DelayUS(u16 x)
{ 
	u8 y;
	while (x--)
		for (y=0; y<PARAMETER_US; y++); 
}

/* *******************************************************************************************
// MS级CPU延时
/* ******************************************************************************************* */
void DelayMS(u16 x)
{
	u16 y;
	while (x--)
		for (y=0; y<PARAMETER_MS; y++); 
}

/* *******************************************************************************************
// 系统状态提示灯闪烁
/* ******************************************************************************************* */
void SysLED_Flash(u16 time, u8 times)
{
    u8 i;
    for (i=0; i<times; i++)
    {
        SysLED_On();
        DelayMS(time);
        SysLED_Off();
        DelayMS(time);
    }
}
